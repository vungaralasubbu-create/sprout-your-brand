// Index for O(1) lookup.
export const PILLAR_MAP: Record<string, Pillar> = Object.fromEntries(
  PILLARS.map((p) => [p.slug, p]),
);

export function listPillars(): Pillar[] {
  return PILLARS;
}

export function getPillar(slug: string): Pillar | undefined {
  return PILLAR_MAP[slug];
}

export function listPillarsByCategory(): Array<{ category: string; items: Pillar[] }> {
  const groups = new Map<string, Pillar[]>();
  for (const p of PILLARS) {
    const g = groups.get(p.category) ?? [];
    g.push(p);
    groups.set(p.category, g);
  }
  return Array.from(groups.entries()).map(([category, items]) => ({ category, items }));
}
