$blog$## The basic idea

In a revenue share model, a partner is paid a defined percentage of the revenue they generate. The company and the partner share the outcome. This is different from a fixed commission or a per-lead payment.

Two things make revenue share work:

1. Both sides win when a real learner enrolls and completes.
2. The percentages, definitions and timing are clear before either side commits.

## What "revenue share" usually covers

Every revenue share needs a definition of:

- **Eligible revenue** — what counts and what does not (refunds, cancellations, taxes, gateway fees).
- **Share percentage** — the partner's cut.
- **Attribution rules** — how a sale is attributed to a partner.
- **Payment cycle** — when the partner is paid.
- **Chargeback treatment** — what happens if a learner refunds.

Where any of these are vague, disputes follow.

## Glintr's two revenue models

Glintr operates two defined revenue models. Both are educational partnerships, not employment.

- **[70% Revenue Model](/70-revenue-model)** — the partner sources their own leads and earns 70% of eligible revenue on enrolments they generate.
- **[50% Supported Model](/50-supported-model)** — the partner works on qualified leads assigned by Glintr and earns 50% of eligible revenue on those enrolments.

Both are exactly what they say — 70% is 70%, 50% is 50%. Neither model guarantees a salary or a specific income.

## Estimating outcomes

Because revenue share depends on conversion volume, an estimator is more honest than a promise. Try the [Income Calculator](/income-calculator) to see how different assumptions produce different outcomes.

